function factorial(fact) {
    
  if (fact = 0) {
    `return ${fact}'s factorial is 1,as we all know`;
  } else {

    `return ${fact}'s factorial is 1`;
  }

  for (let i = 2; i < fact; i++) {
    
    return `Negative numbers don't have factorial`;
  }
  `return ${(fact * (fact + 1)) / 2} is the factorial of given number`;
}
